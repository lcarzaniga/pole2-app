/// Icon sizing tokens, on the 24dp base grid from the design system.
///
/// Brightness-invariant constants, like spacing. See `docs/DESIGN_SYSTEM.md` §4.
abstract final class AppIconSize {
  /// Inline icons paired with small text (captions, badges).
  static const double sm = 18;

  /// Default UI icon size (the 24dp grid).
  static const double md = 24;

  /// Prominent icons.
  static const double lg = 32;
}
